ORG 1000H
MSJ DB "INGRESE UN NUMERO:"
FIN DB ?
ORG 1500H
NUM DB ?
NV DB "CARACTER NO VALIDO"
FINNV DB ?
ORG 3000H
ES_NUM: CMP CL, DL
JZ VERDADERO
INC DL
CMP DL, 39H
JNZ ES_NUM
MOV AH, 00H
CMP AH, 00H
JZ FINAL
VERDADERO: MOV AH, FFH
FINAL: RET
ORG 2000H
MOV BX, OFFSET MSJ
MOV AL, OFFSET FIN-OFFSET MSJ
INT 7
MOV BX, OFFSET NUM
INT 6
MOV AL, 1
INT 7
MOV CL, NUM
MOV DL, 30H
CALL ES_NUM
CMP AH,00H
JNZ EXIT
MOV BX, OFFSET NV
MOV AL, OFFSER FINNV - OFFSET NV
INT 7
EXIT: INT 0
END

